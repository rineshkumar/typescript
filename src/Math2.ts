const addNumbers = (a: number, b: number): number => {
    return a + b;
};
const subractNumbers = (a: number, b: number): number => {
    return a - b;
};
const multiplyNumbes = (a: number, b: number) => {
    return a * b;
};
const divideNumbes = (a: number, b: number) => {
    return a / b;
};
module.exports = {
    addNumbers,
    subractNumbers,
    multiplyNumbes,
    divideNumbes
}